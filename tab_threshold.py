
import streamlit as st

# -------------------------------------------------
# 탭: 파티사이클 클리어 여부 경계값
# - 기존 계산 로직과 독립 (조건/경계값 정보 제공용)
# - 보스는 현재 "사마귀"만 제공
# -------------------------------------------------

def render_threshold_tab():
    st.subheader("📌 파티사이클 클리어 여부 경계값")

    # 보스 선택 (현재 1개)
    boss = st.selectbox("보스 선택", ["사마귀"], index=0)

    # 조건 표시
    st.markdown("### 조건")
    conditions = [
        "게임속도 증가 없음",
        "보스 약화에 따른 딜량 증가를 반영하지 않음",
        "빌드에 능숙한 5인 파티",
        "4페를 어느정도 버틸 수 있을 만큼, 체력 여유가 있는 상태",
    ]
    for c in conditions:
        st.write(f"- {c}")

    st.markdown("---")

    # 경계값 데이터(현재 하드코딩)
    st.markdown("### 클리어 경계값 (사마귀)")
    st.caption("※ 아래 값은 '파티사이클' 기준의 경험적 경계 범위로, 딜 계산 결과와 직접 연결되기보다는 참고용 기준선임")

    # 구분 선택(원하면)
    party_type = st.radio(
        "파티 유형 선택",
        ["빨강(주로 비트 구성)", "빨강(주로 인삼 구성)", "파랑(눈설탕, 캡아 구성)", "노랑(주로 스네 구성)"],
        index=0
    )

    # 표시 로직
    if party_type == "빨강(주로 비트 구성)":
        st.success("클리어 경계값: **105 ~ 110회**")
        st.write("- 105회 이하면 안정권에 가까움")

    elif party_type == "빨강(주로 인삼 구성)":
        st.warning("클리어 경계값: **데이터 없음**")

    elif party_type == "파랑(눈설탕, 캡아 구성)":
        st.success("클리어 경계값: **105 ~ 110회**")
        st.write("- 105회 이하면 안정권에 가까움")

    elif party_type == "노랑(주로 스네 구성)":
        st.info("클리어 경계값: **155회 내외**")
        st.write("- 155회 안쪽이면 클리어 가능성이 높음")

    st.markdown("---")
